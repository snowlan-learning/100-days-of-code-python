# This Python script explains the usage of Logical Operators in Python.
# The primary logical operators are: and, or, not

print("Explaining Logical Operators in Python\n")

# === AND Operator ===
print("=== AND Operator ===")
print("The 'and' operator returns True if both the operands are true.")

a = True
b = False

print("a =", a)
print("b =", b)

print("Result of a and b:", a and b)

print("\n-----------------------------\n")

# === OR Operator ===
print("=== OR Operator ===")
print("The 'or' operator returns True if any of the operands is true.")

a = True
b = False

print("a =", a)
print("b =", b)

print("Result of a or b:", a or b)

print("\n-----------------------------\n")

# === NOT Operator ===
print("=== NOT Operator ===")
print("The 'not' operator returns True if the operand is false and returns False if the operand is true.")

a = True
b = False

print("a =", a)
print("b =", b)

print("Result of not a:", not a)
print("Result of not b:", not b)
