# Python Conditional Operators Explained

# Conditional operators, also known as comparison operators, are used
# to compare two values and evaluate down to a single Boolean value.

# ------------------------------
# Equality Operator (==)
# ------------------------------

# Checks if the value of two operands are equal or not.
equal_result = (5 == 5)
print(f"Equality Operator: 5 == 5 returns {equal_result}")

# ------------------------------
# Not Equal Operator (!=)
# ------------------------------

# Checks if the value of two operands are not equal.
not_equal_result = (5 != 3)
print(f"Not Equal Operator: 5 != 3 returns {not_equal_result}")

# ------------------------------
# Greater Than Operator (>)
# ------------------------------

# Checks if the value of the left operand is greater than the value of the right operand.
greater_than_result = (5 > 3)
print(f"Greater Than Operator: 5 > 3 returns {greater_than_result}")

# ------------------------------
# Less Than Operator (<)
# ------------------------------

# Checks if the value of the left operand is less than the value of the right operand.
less_than_result = (3 < 5)
print(f"Less Than Operator: 3 < 5 returns {less_than_result}")

# ------------------------------
# Greater Than or Equal To Operator (>=)
# ------------------------------

# Checks if the value of the left operand is greater than or equal to the value of the right operand.
greater_than_equal_result = (5 >= 5)
print(f"Greater Than or Equal To Operator: 5 >= 5 returns {greater_than_equal_result}")

# ------------------------------
# Less Than or Equal To Operator (<=)
# ------------------------------

# Checks if the value of the left operand is less than or equal to the value of the right operand.
less_than_equal_result = (5 <= 5)
print(f"Less Than or Equal To Operator: 5 <= 5 returns {less_than_equal_result}")

# ------------------------------
# Chaining Comparisons
# ------------------------------

# You can chain multiple comparisons for more complex conditions.
chained_result = (3 < 5 <= 6)
print(f"Chaining Comparisons: 3 < 5 <= 6 returns {chained_result}")

