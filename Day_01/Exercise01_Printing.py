# Basic Usage of Print Function
# The following line demonstrates how to print a string with a new line character (\n) 
# to separate "Hello World!" into two lines.
print("Hello World!\nHello World!")

# Explanation of New Line Character
# The special character '\n' is used to insert a new line in the output.

# String Concatenation in Print Function
# Demonstrates how to concatenate multiple strings using the '+' operator.
# Also includes a new line character (\n) at the end.

print("Hello" + " " + "Sam\n")


